# Este programa calcula o salário com reajuste de 15%
# Exibe mensagem para o usuário
print("salario com reasjuste")
# Recebe o salário inicial
salario = float(input('salario inicial: '))
# Calcula o novo salário com reajuste de 15%
novoSalario = salario + (salario * 0.15)
# Exibe o novo salário
print(novoSalario)