# Este programa calcula o volume de uma caixa retangular
# Recebe o valor do comprimento
comprimento = float(input('valor de comprimento: '))
# Recebe o valor da largura
largura = float(input('valor de comprimento: '))
# Recebe o valor da altura
altura = float(input('valor de comprimento: '))
# Calcula o volume
volume = altura * comprimento * largura
# Exibe o volume
print(volume)