# Este programa calcula a diferença entre dois números reais
# Recebe o primeiro número real
num1 = float(input("Digite o primeiro número real: "))
# Recebe o segundo número real
num2 = float(input("Digite o segundo número real: "))
# Calcula a diferença
diferenca = num1 - num2
# Exibe o resultado
print("A diferença entre os valores é:", diferenca)