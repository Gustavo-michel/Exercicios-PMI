# Este programa calcula a soma dos quadrados de dois números inteiros
# Recebe o primeiro número inteiro
num1 = int(input("Digite o primeiro número inteiro: "))
# Recebe o segundo número inteiro
num2 = int(input("Digite o segundo número inteiro: "))
# Calcula a soma dos quadrados
soma_quadrados = num1**2 + num2**2
# Exibe o resultado
print("A soma dos quadrados é:", soma_quadrados)

