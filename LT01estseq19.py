# Este programa recebe dois valores reais e mostra o maior deles
# Recebe o primeiro número real
num1 = float(input("numero 1: "))
# Recebe o segundo número real
num2 = float(input("numero 2: "))
# Verifica qual é o maior
if num1 > num2:
    print(f"O maior é: {num1}")
elif num1 < num2:
    print(f"O maior é: {num2}")
else:
    print("Numeros iguais")
    print("Numeros iguais")
